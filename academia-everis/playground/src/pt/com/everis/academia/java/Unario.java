package pt.com.everis.academia.java;

public class Unario {

	public static void main(String[] args) {

		int result = +1;
		System.out.println(result);
		
		result--;
		System.out.println(result);
		
		result++;
		System.out.println(result);
		
		result = -result;
		System.out.println(result);
		boolean succcess = false;
		
		System.out.println(succcess);
		
		System.out.println(!succcess);

	}

}
